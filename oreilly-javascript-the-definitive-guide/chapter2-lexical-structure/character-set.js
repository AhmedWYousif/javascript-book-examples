
// JavaScript programs are written using the Unicode character set. 
// Unicode is a superset of ASCII and Latin-1
// JavaScript is a case-sensitive language
// This means that language keywords, variables,
// function names, and other identifiers must always be typed with a consistent capitalization
// of letters. The while keyword, for example, must be typed “while,” not “While”
// however, that HTML is not case-sensitive (although XHTML is).

// JavaScript ignores spaces that appear between tokens in programs.
// In addition to the regular space character (\u0020), 
// JavaScript also recognizes the following characters as whitespace: 
// tab (\u0009), 
// vertical tab (\u000B), 
// form feed (\u000C), 
// nonbreaking space (\u00A0), 
// byte order mark (\uFEFF), 
// and any character in Unicode category Zs. JavaScript recognizes the following characters as line terminators:
// line feed (\u000A), 
// carriage return (\u000D), 
// line separator (\u2028), 
// and paragraph separator (\u2029). 
// A carriage return, line feed sequence is treated as a single line terminator.

// The Unicode escape for the character é, for example, is
// \u00E9, and the following two JavaScript strings are identical:
"café" === "caf\u00e9" // => true